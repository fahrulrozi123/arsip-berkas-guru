    // Fungsi untuk mendapatkan halaman sebelumnya yang dikunjungi
    function getPreviousPageURL() {
      var previousPageURL = sessionStorage.getItem('previousPageURL');
      if (previousPageURL) {
        return previousPageURL;
      }
      return '/default-page'; // Ganti '/default-page' dengan URL halaman default jika tidak ada halaman sebelumnya yang dikunjungi
    }

    // Fungsi untuk menyimpan URL halaman saat ini
    function saveCurrentPageURL() {
      sessionStorage.setItem('previousPageURL', window.location.href);
    }

    // Fungsi untuk pergi ke halaman sebelumnya
    function goToPreviousPage() {
      window.history.back();
    }

    // Fungsi untuk pergi ke halaman berikutnya
    function goToNextPage() {
      var previousPageURL = getPreviousPageURL();
      var nextPageURL = sessionStorage.getItem('nextPageURL');
      if (nextPageURL) {
        window.location.href = nextPageURL;
      } else {
        window.location.href = previousPageURL;
      }
    }

    // Panggil fungsi saveCurrentPageURL() saat halaman dimuat
    window.addEventListener('load', function() {
      saveCurrentPageURL();
    });
